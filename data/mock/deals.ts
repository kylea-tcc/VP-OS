// ─── Computed helpers ─────────────────────────────────────────────────────────

export const savedDeals = mockDeals.filter((d) => d.isSaved);

export const dealsByBucket = {
  atlas: mockDeals.filter((d) => d.pipelineBucket === "atlas"),
  renewals: mockDeals.filter((d) => d.pipelineBucket === "renewals"),
  core: mockDeals.filter((d) => d.pipelineBucket === "core"),
};

export function getPipelineSummary(bucket: PipelineBucket) {
  const deals = dealsByBucket[bucket];
  const activeDeal = deals.filter((d) => !["closed_won", "closed_lost"].includes(d.dealStageId));
  return {
    totalDeals: activeDeal.length,
    totalPipeline: activeDeal.reduce((s, d) => s + d.amount, 0),
    weightedPipeline: activeDeal.reduce((s, d) => s + d.weightedAmount, 0),
    commitPipeline: activeDeal
      .filter((d) => d.forecastCategory === "commit")
      .reduce((s, d) => s + d.amount, 0),
    atRisk: activeDeal.filter((d) => d.riskStatus === "at_risk").length,
    stalled: activeDeal.filter((d) => d.riskStatus === "stalled").length,
    noNextStep: activeDeal.filter((d) => !d.nextStep).length,
  };
}
